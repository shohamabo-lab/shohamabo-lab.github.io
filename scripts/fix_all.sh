#!/bin/bash

echo "🔧 מתחיל תיקון שגיאות באתר מנפחים ת'כיס..."
echo ""

# ========================================
# תיקון 1: metchilim.html — "שאתם שצריכים" כפול
# ========================================
sed -i 's/המדריך שאתם שצריכים לדעת/המדריך שאתם צריכים לדעת/g' metchilim.html
echo "✅ metchilim.html — תוקן: 'שאתם שצריכים' → 'שאתם צריכים'"

# ========================================
# תיקון 2: psychology.html — כותרת "הרגלים" → "ההרגלים"
# ========================================
sed -i 's/הרגלים הבאים הם שיעור הכי חשוב שלא למדו אתכם בבית הספר\./ההרגלים הבאים הם השיעור הכי חשוב שלא לימדו אתכם בבית הספר./g' psychology.html
echo "✅ psychology.html — תוקן: 'הרגלים' → 'ההרגלים', 'שיעור' → 'השיעור', 'למדו' → 'לימדו'"

# ========================================
# תיקון 3: psychology.html — שורה תחתונה פגומה
# ========================================
sed -i 's/אתם עברים אדירים\. עשור בזמן נחוש נסוגה, מחוקי ופסיבי שמנפחים לכם את הכיס\./הנכס הכי יקר שתוכלו לקנות הוא לא בית מאבן, אלא את הבעלות על הזמן שלכם. תשקיעו כדי לקנות בחזרה את השעות והימים שלכם, כי זה הניצחון האמיתי./g' psychology.html
echo "✅ psychology.html — תוקנה שורה תחתונה פגומה"

# ========================================
# תיקון 4: assets.html — "נצמרות" → "מצטברים"
# ========================================
sed -i 's/תוספות שנצמרות לכסף שלך/רווחים שמצטברים על הכסף שלך/g' assets.html
echo "✅ assets.html — תוקן: 'נצמרות' → 'מצטברים'"

# ========================================
# תיקון 5: bankayt.html — רווח חסר בכותרת
# ========================================
sed -i 's/איך לדבר בנקאיתולהישאר/איך לדבר בנקאית ולהישאר/g' bankayt.html
echo "✅ bankayt.html — תוקן: רווח חסר בכותרת הדף"

# ========================================
# תיקון 6: index.html — כפתור צף, אימוג'י מימין לשמאל
# ========================================
sed -i 's/💰 להתחיל/להתחיל 💰/g' index.html
echo "✅ index.html — תוקן כפתור צף: אימוג'י עבר לצד שמאל"

echo ""
echo "🎉 כל התיקונים הושלמו בהצלחה!"
echo ""
echo "📤 עכשיו תריץ:"
echo "   git add -A && git commit -m 'fix: תיקון שגיאות עברית וכפתור צף' && git push"
